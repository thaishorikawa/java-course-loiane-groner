package com.javacourse.loiane.lesson13;

public class ArithmeticOperators {

    public static void main(String[] args) {

        int result = 1 + 2;
        System.out.println(result);

        result = result - 1;
        System.out.println(result);

        result = result * 2;
        System.out.println(result);

        result = result / 2;
        System.out.println(result);

        result = result + 8;
        System.out.println(result);

        result = result % 7;
        System.out.println(result);

        result += 1;
        System.out.println(result++);

        result -= 3;
        System.out.println(result);

        System.out.println(result--);
        System.out.println(--result);
        
    }
}
