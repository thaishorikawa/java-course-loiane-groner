package com.javacourse.loiane.lesson13;

public class RelationalOperators {

    public static void main(String[] args) {

    int number1 = 1;
    int number2 = 2;

    System.out.println("Number 1 == Number 2: " + (number1 == number2));
    System.out.println("Number 1 != Number 2: " + (number1 != number2));
    System.out.println("Number 1 > Number 2: " + (number1 > number2));
    System.out.println("Number 1 >= Number 2: " + (number1 >= number2));
    System.out.println("Number 1 < Number 2: " + (number1 < number2));
    System.out.println("Number 1 <= Number 2: " + (number1 <= number2));

    }

}
